SELECT SUBSTRING(DIAG_01, 0, 4),
COUNT (*) admissions
FROM HES_APC.dbo.[vHES_APC_SAV]
WHERE EPIORDER = 1
AND EPISTAT = 3
AND ADMIMETH IN ('21', '22', '23', '24', '25', '2A', '2B', '2C', '2D')
AND DIAG_01 LIKE 'F1%'
AND DIAG_01 NOT LIKE 'F10%'
AND DIAG_01 NOT LIKE 'F17%'
AND FYEAR IN ('0203', '0304', '0405', '0506', '0607', '0708', '0809', '0910', '1011', '1112', '1213', '1314', '1415', '1516', '1617', '1718', '1819', '1920', '2021', '2122')
GROUP BY SUBSTRING(DIAG_01, 0, 4)
