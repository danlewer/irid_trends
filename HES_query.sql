(
	SELECT DISTINCT EPIKEY, FYEAR
	FROM HES_APC.dbo.vtHES_APC_DIAG
	WHERE DiagCode4 LIKE 'F11%'
) as opi

ON main.EPIKEY = opi.EPIKEY
AND main.FYEAR = opi.FYEAR

WHERE (
DIAG4_01 LIKE 'L02%'    /* Abscess */
OR DIAG4_01 LIKE 'L03%' /* Cellulitis */
OR DIAG4_01 LIKE 'I80%' /* Phlebitis & thrombophlebitis */
OR DIAG4_01 LIKE 'A48%' /* Other SSTI */
OR DIAG4_01 LIKE 'L088'
OR DIAG4_01 LIKE 'L089'
OR DIAG4_01 LIKE 'L97%'
OR DIAG4_01 LIKE 'L984'
OR DIAG4_01 LIKE 'L988'
OR DIAG4_01 LIKE 'L989'
OR DIAG4_01 LIKE 'R02%'
OR DIAG4_01 LIKE 'B376' /* Endocarditis */
OR DIAG4_01 LIKE 'I330'
OR DIAG4_01 LIKE 'I339'
OR DIAG4_01 LIKE 'I38%'
OR DIAG4_01 LIKE 'I39%'
OR DIAG4_01 LIKE 'A40%' /* Septicaemia */
